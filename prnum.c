#include <unistd.h>

int	prnum(int n)
{
	char	s;
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		i = 11;
		return (i);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		i++;
	}
	if (n > 9)
	{
		prnum(n / 10);
		i++;
	}
	s = '0' + n % 10;
	write(1, &s, 1);
	return (i + 1);

}
